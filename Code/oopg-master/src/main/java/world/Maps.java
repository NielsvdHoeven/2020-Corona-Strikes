    /**
     * checkt of alle humans in het spel geinfect zijn
     */
    public boolean checkAllInfected(Human[] humans) {
        for (int i = 0; i < humans.length; i++) {
            if (!humans[i].getInfected()) {
                return false;
            }
        }
        return true;
    }

